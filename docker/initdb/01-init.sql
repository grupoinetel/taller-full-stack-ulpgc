CREATE TABLE USUARIOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150),
    apellido VARCHAR(255),
    nombreUsuario VARCHAR(150),
    avatar VARCHAR(255)
);

CREATE TABLE TAREAS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    numTarea NUMBER,
    prioridad NUMBER,
    STATE VARCHAR(255),
    descripcion VARCHAR(255),
    fechaFinalizacion DATE,
    fechaCreacion DATE,
    porcentaje NUMBER,
    autor NUMBER FOREIGN KEY
);

CREATE TABLE COMMENTS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    mensaje VARCHAR(255),
    tarea NUMBER FOREIGN KEY,
    autor NUMBER FOREIGN KEY,
    fechaCreacion DATE
);
