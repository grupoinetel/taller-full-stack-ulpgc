CREATE TABLE USUARIOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(150),
    SURNAME VARCHAR(255),
    USERBAME VARCHAR(150),
    AVATAR VARCHAR(255)
);

CREATE TABLE TAREAS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NUMTAREA NUMBER,
    PRIORITY NUMBER,
    STATE VARCHAR(255),
    DESCRIPTION VARCHAR(255),
    FINAL_DATE DATE,
    CREATION_DATE DATE,
    PERCENTAGE NUMBER,
    AUTHOR NUMBER FOREIGN KEY
);

CREATE TABLE COMMENTS (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MESSAGE VARCHAR(255),
    TAREA NUMBER FOREIGN KEY,
    AUTHOR NUMBER FOREIGN KEY,
    CREATION_DATE DATE
);