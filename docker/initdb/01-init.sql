CREATE TABLE USUARIOS (
    ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150),
    apellido VARCHAR(255),
    nombreUsuario VARCHAR(150),
    avatar VARCHAR(255)
);

CREATE TABLE TAREAS (
    ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    numTarea INT,
    prioridad INT,
    STATE VARCHAR(255),
    descripcion VARCHAR(255),
    fechaFinalizacion DATE,
    fechaCreacion DATE,
    porcentaje INT,
    autor INT,
    FOREIGN KEY (autor) REFERENCES USUARIOS(ID)
);

CREATE TABLE COMENTARIOS (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    mensaje VARCHAR(255),
    tarea INT,
    autor INT,
    fechaCreacion DATE,
    FOREIGN KEY (tarea) REFERENCES TAREAS(ID),
    FOREIGN KEY (autor) REFERENCES USUARIOS(ID)
);

CREATE TABLE USUARIOS_TAREAS (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    idUsuario INT,
    idTarea INT,
    FOREIGN KEY (idUsuario) REFERENCES USUARIOS(ID),
    FOREIGN KEY (idTarea) REFERENCES TAREAS(ID)
);
